# ejercicio 1 (while): suma de numeros hasta ingresar 0 
# estimado alumno realiza el ejercicio con tu numero favorito para terminar 
suma = 0 
num = int(input("ingresa un numero entero (0 para terminar : "))
while num != 0: 
    suma += num 
    num = int(input("3 (0)"))
print("la suma de los numeros ingresados es:", suma )


